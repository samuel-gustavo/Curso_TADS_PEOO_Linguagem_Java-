    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String item = jTextField3.getText();
        DefaultListModel<String> modeloOriginal = (DefaultListModel<String>) jList1.getModel();
        
        if(!item.isEmpty()) {
            modeloOriginal.addElement(item);
            jList1.setModel(modeloOriginal);
            jTextField3.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Informe um valor para inserir na Lista!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

