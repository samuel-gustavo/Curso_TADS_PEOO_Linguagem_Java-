    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = jTextField1.getText();
        String senha = new String(jPasswordField1.getPassword());
        
        if(usuario.equalsIgnoreCase(this.usuario) && senha.equalsIgnoreCase(this.senha)) {
            JOptionPane.showMessageDialog(null, "Autenticado com Sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Usuário ou Senha Incorretos! " + senha);
        }
        
        jTextField1.setText("");
        jPasswordField1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

